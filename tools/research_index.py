def audit_source_drift_report(root: Path, manifest: dict, database: Path) -> str:
    with closing(sqlite3.connect(database)) as connection:
        audit_hashes = {path: digest for path, digest in connection.execute("SELECT path,sha256 FROM documents")}
    lines = ["# Audit and source hash drift", "", "> Inventory only; `research_index.py check` remains the enforcement path for curated audited claims.", "", "| Claim | Source | Source hash | Audit hash | State |", "|---|---|---|---|---|"]
    for claim in sorted((item for item in manifest["claims"] if item.get("audit")), key=lambda item: item["id"]):
        actual = sha256_file(root / claim["source"])
        expected = claim.get("expected_source_sha256")
        audit_path = claim["audit"]
        audit_text = (root / audit_path).read_text(encoding="utf-8", errors="replace")
        state = "current" if expected == actual and actual in audit_text and _has_explicit_green_verdict(audit_text) else "DRIFT OR SCOPE REVIEW"
        lines.append(f"| `{claim['id']}` | `{claim['source']}` | `{actual}` | `{audit_hashes.get(audit_path, 'missing')}` | {state} |")
    lines.append("")
    return "\n".join(lines)


def corpus_coverage_report(database: Path) -> str:
    with closing(sqlite3.connect(database)) as connection:
        areas = connection.execute("SELECT area,COUNT(*) FROM documents GROUP BY area ORDER BY area").fetchall()
        counts = dict(connection.execute("SELECT 'documents',COUNT(*) FROM documents UNION ALL SELECT 'sections',COUNT(*) FROM sections UNION ALL SELECT 'claims',COUNT(*) FROM claims UNION ALL SELECT 'relations',COUNT(*) FROM relations UNION ALL SELECT 'candidate_claims',COUNT(*) FROM candidate_claims UNION ALL SELECT 'candidate_relations',COUNT(*) FROM candidate_relations UNION ALL SELECT 'explicit_statuses',COUNT(*) FROM explicit_statuses UNION ALL SELECT 'audit_pairs',COUNT(*) FROM audit_pairs UNION ALL SELECT 'aliases',COUNT(*) FROM terminology_aliases UNION ALL SELECT 'connections',COUNT(*) FROM connection_candidates"))
        strict_pairs = connection.execute("SELECT COUNT(DISTINCT source_document_id) FROM audit_pairs WHERE green_verdict=1 AND source_hash_bound=1 AND extraction_method LIKE '%adjacent-filename%'").fetchone()[0]
        invalid_links = connection.execute("SELECT COUNT(*) FROM links WHERE valid=0").fetchone()[0]
        metadata = connection.execute("SELECT COUNT(*),SUM(source_heading IS NOT NULL),SUM(scope IS NOT NULL),SUM(expected_source_sha256 IS NOT NULL) FROM claims").fetchone()
    lines = ["# Corpus coverage statistics", "", "> Generated coverage measures structure and provenance, not mathematical truth.", "", "## Documents by area", "", "| Area | Documents |", "|---|---:|"]
    lines.extend(f"| {area} | {count} |" for area, count in areas)
    lines.extend(["", "## Generated records", "", "| Record type | Count |", "|---|---:|"])
    lines.extend(f"| {name} | {count} |" for name, count in sorted(counts.items()))
    lines.extend(["", f"- Distinct strict audit-pair source candidates: **{strict_pairs}**.", f"- Conventionally invalid Markdown links (historical snapshots included): **{invalid_links}**.", f"- Curated claim metadata: {metadata[1]}/{metadata[0]} source headings, {metadata[2]}/{metadata[0]} scopes, {metadata[3]}/{metadata[0]} pinned source hashes.", ""])
    return "\n".join(lines)


def context_pack(manifest: dict, claim_id: str, database: Path | None = None) -> str:
    claims = _claim_map(manifest)
    if claim_id not in claims:
        raise IntegrityError(f"unknown claim id: {claim_id}")
    claim = claims[claim_id]
    outgoing, incoming = _relation_groups(manifest)
    lines = [GENERATED_CONTEXT_MARKER, "", f"# Context: {claim['title']}", "", "> Non-authoritative; generated from the curated manifest.", "", f"- **Claim ID:** `{claim_id}`", f"- **Status:** {claim['status']}", f"- **Source:** `{claim['source']}`"]
    if claim.get("strict_parameter"):
        lines.append(f"- **Declared strict parameter:** {claim['strict_parameter']}")
    for heading, key in (("Hypotheses", "hypotheses"), ("Allowed exits", "allowed_exits"), ("Trust boundary", "does_not_prove")):
        values = claim.get(key, [])
        if values:
            lines.extend(["", f"## {heading}", ""] + [f"- {value}" for value in values])
    dependencies = [relation for relation in outgoing.get(claim_id, []) if relation["kind"] in PROOF_RELATIONS]
    if dependencies:
        lines.extend(["", "## Direct prerequisites", ""])
        for relation in sorted(dependencies, key=lambda item: item["target"]):
            dependency = claims[relation["target"]]
            lines.append(f"- `{dependency['id']}` — {dependency['title']} ({dependency['status']})")
        closure: set[str] = set()
        queue = deque(relation["target"] for relation in dependencies)
        while queue:
            dependency_id = queue.popleft()
            if dependency_id in closure:
                continue
            closure.add(dependency_id)
            queue.extend(
                relation["target"]
                for relation in outgoing.get(dependency_id, [])
                if relation["kind"] in PROOF_RELATIONS
            )
        lines.extend([
            "",
            "## Curated proved-dependency closure",
            "",
            "This is the transitive closure of explicit `uses` relations in the manifest, not an independent completeness claim.",
            "",
        ])
        for dependency_id in sorted(closure):
            dependency = claims[dependency_id]
            lines.append(f"- `{dependency_id}` — {dependency['title']} ({dependency['status']})")
    parents = [relation for relation in outgoing.get(claim_id, []) if relation["kind"] == "subproblem_of"]
    if parents:
        lines.extend(["", "## Programme scope", ""])
        for relation in sorted(parents, key=lambda item: item["target"]):
            parent = claims[relation["target"]]
            lines.append(f"- Subproblem of `{parent['id']}` — {parent['title']} ({parent['status']})")
    refinements: list[tuple[int, str]] = []
    refinement_queue = deque(
        (1, relation["source"])
        for relation in sorted(incoming.get(claim_id, []), key=lambda item: item["source"])
        if relation["kind"] == "subproblem_of"
        and claims[relation["source"]]["status"] == "conditional-target"
    )
    seen_refinements: set[str] = set()
    while refinement_queue:
        depth, refinement_id = refinement_queue.popleft()
        if refinement_id in seen_refinements:
            continue
        seen_refinements.add(refinement_id)
        refinements.append((depth, refinement_id))
        refinement_queue.extend(
            (depth + 1, relation["source"])
            for relation in sorted(
                incoming.get(refinement_id, []), key=lambda item: item["source"]
            )
            if relation["kind"] == "subproblem_of"
            and claims[relation["source"]]["status"] == "conditional-target"
        )
    if refinements:
        lines.extend(["", "## Conditional refinements and laboratories", ""])
        for depth, refinement_id in refinements:
            refinement = claims[refinement_id]
            indent = "  " * (depth - 1)
            lines.append(
                f"{indent}- `{refinement_id}` — {refinement['title']} "
                f"({refinement['status']})"
            )
    barriers = [relation for relation in outgoing.get(claim_id, []) if relation["kind"] in {"refuted_by", "sharpness_witness", "related_to", "guarded_by"} and claims[relation["target"]]["kind"] == "barrier"]
    barriers.extend(relation for relation in incoming.get(claim_id, []) if claims[relation["source"]]["kind"] == "barrier")
    if barriers:
        lines.extend(["", "## Directly relevant barriers", ""])
        seen: set[str] = set()
        for relation in sorted(barriers, key=lambda item: (item["source"], item["target"])):
            barrier_id = relation["target"] if claims[relation["target"]]["kind"] == "barrier" else relation["source"]
            if barrier_id in seen:
                continue
            seen.add(barrier_id)
            lines.append(f"- `{barrier_id}` — {claims[barrier_id]['title']}")
    if database and database.exists() and claim.get("keywords"):
        query = " OR ".join(f'"{keyword}"' for keyword in claim["keywords"][:4])
        leads = search_index(database, query, None, 10)
        if leads:
            lines.extend(["", "## Full-text discovery leads", "", "These lexical matches are search hints, not proof dependencies.", ""])
            for path, heading, start, end, _snippet in leads:
                lines.append(f"- `{path}:{start}` — {heading or '(preamble)'} (lines {start}–{end})")
    lines.append("")
    return "\n".join(lines)


def generate_reports(root: Path, manifest: dict, database: Path) -> list[Path]:
    output = database.parent
    output.mkdir(parents=True, exist_ok=True)
    reports = {
        "proof_dag.md": proof_dag(manifest),
        "trust_boundaries.md": trust_boundaries(manifest),
        "barrier_matrix.md": barrier_matrix(manifest),
        "orphaned_audited_results.md": orphaned_audited_results(database),
        "duplicate_supersession_candidates.md": duplicate_supersession_candidates(database),
        "terminology_aliases.md": terminology_alias_report(database),
        "connection_candidates.md": connection_candidate_report(database),
        "audit_source_drift.md": audit_source_drift_report(root, manifest, database),
        "corpus_coverage.md": corpus_coverage_report(database),
    }
    context_reports: dict[str, str] = {}
    for claim in sorted((item for item in manifest["claims"] if item["kind"] == "target" and item["active"]), key=lambda item: item["id"]):
        safe = re.sub(r"[^A-Za-z0-9_.-]", "_", claim["id"])
        context_reports[f"context_{safe}.md"] = context_pack(
            manifest, claim["id"], database
        )
    reports.update(context_reports)

    # Generate every file successfully before publishing any of them.  Each
    # replacement is atomic, and only report filenames owned by this program
    # are touched; the database and unrelated cache files remain intact.
    with tempfile.TemporaryDirectory(prefix=".reports.", dir=output) as staging_name:
        staging = Path(staging_name)
        for name, content in sorted(reports.items()):
            (staging / name).write_text(content, encoding="utf-8")
        for name in sorted(reports):
            os.replace(staging / name, output / name)

    expected_contexts = set(context_reports)
    for path in output.glob("context_*.md"):
        if path.name in expected_contexts or not path.is_file():
            continue
        try:
            with path.open(encoding="utf-8", errors="replace") as stream:
                opening = [stream.readline().rstrip("\n") for _ in range(3)]
        except OSError:
            continue
        owned = opening[0] == GENERATED_CONTEXT_MARKER or (
            opening[0].startswith("# Context: ")
            and opening[1] == ""
            and opening[2]
            == "> Non-authoritative; generated from the curated manifest."
        )
        if owned:
            path.unlink()

    return [output / name for name in reports]


def run_verifiers(root: Path, manifest: dict, selected: set[str] | None = None) -> None:
    known = {verifier["id"] for verifier in manifest["verifiers"]}
    unknown = (selected or set()) - known
    if unknown:
        raise IntegrityError(f"unknown verifier id(s): {', '.join(sorted(unknown))}")
    for verifier in manifest["verifiers"]:
        if selected is not None and verifier["id"] not in selected:
            continue
        command = [sys.executable, verifier["path"]]
        start = time.monotonic()
        try:
            result = subprocess.run(command, cwd=root, check=False, capture_output=True, text=True, timeout=verifier["timeout"], shell=False)
        except subprocess.TimeoutExpired as exc:
            raise IntegrityError(f"verifier {verifier['id']} timed out after {verifier['timeout']}s") from exc
        elapsed = time.monotonic() - start
        if result.returncode:
            raise IntegrityError(f"verifier {verifier['id']} failed with exit {result.returncode}: {result.stderr.strip()}")
        actual = result.stdout.splitlines()
        if actual != verifier["expected_stdout"]:
            raise IntegrityError(f"verifier {verifier['id']} output mismatch:\nexpected {verifier['expected_stdout']!r}\nactual   {actual!r}")
        if result.stderr.strip():
            raise IntegrityError(f"verifier {verifier['id']} wrote to stderr: {result.stderr.strip()}")
        print(f"PASS {verifier['id']} ({elapsed:.2f}s)")


def print_errors(errors: Sequence[str]) -> int:
    if not errors:
        print("Research integrity check: PASS")
        return 0
    print("Research integrity check: FAIL", file=sys.stderr)
    for error in errors:
        print(f"- {error}", file=sys.stderr)
    return 1


def main(argv: Sequence[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    subparsers = parser.add_subparsers(dest="command", required=True)
    subparsers.add_parser("check", help="validate the curated spine and tracked corpus")
    build_parser = subparsers.add_parser("build", help="rebuild the disposable SQLite/FTS index")
    build_parser.add_argument("--output", type=Path, default=DEFAULT_DATABASE)
    search_parser = subparsers.add_parser("search", help="search indexed Markdown sections")
    search_parser.add_argument("query")
    search_parser.add_argument("--database", type=Path, default=DEFAULT_DATABASE)
    search_parser.add_argument("--area", choices=("root", "active", "results", "barriers", "archive"))
    search_parser.add_argument("--limit", type=int, default=10)
    report_parser = subparsers.add_parser("report", help="generate non-authoritative proof-spine reports")
    report_parser.add_argument("--database", type=Path, default=DEFAULT_DATABASE)
    context_parser = subparsers.add_parser("context", help="generate one target context pack")
    context_parser.add_argument("claim_id")
    context_parser.add_argument("--database", type=Path, default=DEFAULT_DATABASE)
    verifier_parser = subparsers.add_parser("verify", help="run the small deterministic verifier whitelist")
    verifier_parser.add_argument("--id", action="append", dest="ids")
    verifier_parser.add_argument("--ids-file", type=Path)
    ci_parser = subparsers.add_parser("ci", help="run check, temporary index/report build, and verifiers")
    ci_parser.add_argument("--skip-verifiers", action="store_true")
    args = parser.parse_args(argv)

    try:
        manifest = load_manifest()
        if args.command == "check":
            return print_errors(validate_repository())
        if args.command == "build":
            path = build_index(output=args.output)
            print(path)
            return 0
        if args.command == "search":
            ensure_fresh_index(args.database)
            for path, heading, start, end, snippet in search_index(args.database, args.query, args.area, args.limit):
                print(f"{path}:{start}-{end}\t{heading or '(preamble)'}\t{snippet.replace(chr(10), ' ')}")
            return 0
        if args.command == "report":
            ensure_fresh_index(args.database)
            for path in generate_reports(ROOT, manifest, args.database):
                print(path)
            return 0
        if args.command == "context":
            ensure_fresh_index(args.database)
            print(context_pack(manifest, args.claim_id, args.database), end="")
            return 0
        if args.command == "verify":
            errors = validate_repository()
            if errors:
                return print_errors(errors)
            selected = set(args.ids or [])
            if args.ids_file:
                selected.update(
                    line.strip()
                    for line in args.ids_file.read_text(encoding="utf-8").splitlines()
                    if line.strip()
                )
            run_verifiers(
                ROOT,
                manifest,
                selected if args.ids or args.ids_file else None,
            )
            return 0
        if args.command == "ci":
            errors = validate_repository()
            if errors:
                return print_errors(errors)
            with tempfile.TemporaryDirectory(prefix="hc7-research-index-") as directory:
                database = Path(directory) / "index.sqlite"
                build_index(output=database)
                generate_reports(ROOT, manifest, database)
                if not search_index(database, '"component defect"', None, 3):
                    raise IntegrityError("FTS smoke query returned no component-defect document")
            if not args.skip_verifiers:
                run_verifiers(ROOT, manifest)
            print("Research integrity CI: PASS")
            return 0
    except (IntegrityError, OSError, sqlite3.Error, tomllib.TOMLDecodeError) as exc:
        print(f"ERROR: {exc}", file=sys.stderr)
        return 1
    return 2


if __name__ == "__main__":
    raise SystemExit(main())
