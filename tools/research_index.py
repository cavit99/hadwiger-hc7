def sha256_file(path: Path) -> str:
    digest = hashlib.sha256()
    with path.open("rb") as stream:
        for block in iter(lambda: stream.read(1024 * 1024), b""):
            digest.update(block)
    return digest.hexdigest()


def run_git(root: Path, *args: str) -> bytes:
    result = subprocess.run(
        ["git", *args], cwd=root, check=False, capture_output=True
    )
    if result.returncode:
        raise IntegrityError(
            f"git {' '.join(args)} failed: {result.stderr.decode(errors='replace').strip()}"
        )
    return result.stdout


def tracked_paths(root: Path) -> tuple[str, ...]:
    raw = run_git(root, "ls-files", "-z")
    return tuple(sorted(item.decode() for item in raw.split(b"\0") if item))


def git_head(root: Path) -> str:
    return run_git(root, "rev-parse", "HEAD").decode().strip()


def corpus_signature(root: Path, manifest_path: Path, tracked: Sequence[str] | None = None) -> str:
    """Hash the exact tracked Markdown corpus plus the curated manifest."""
    paths = tracked if tracked is not None else tracked_paths(root)
    digest = hashlib.sha256()
    digest.update(b"research-index-schema-2\0")
    digest.update(Path(__file__).read_bytes())
    digest.update(b"\0")
    digest.update(manifest_path.read_bytes())
    digest.update(b"\0")
    discovery_path = root / "tools" / "research_discovery.toml"
    if discovery_path.exists():
        digest.update(discovery_path.read_bytes())
        digest.update(b"\0")
    for relative in paths:
        if not relative.endswith(".md"):
            continue
        digest.update(relative.encode("utf-8"))
        digest.update(b"\0")
        digest.update((root / relative).read_bytes())
        digest.update(b"\0")
    return digest.hexdigest()


def load_manifest(path: Path = MANIFEST_PATH) -> dict:
    with path.open("rb") as stream:
        return tomllib.load(stream)


def load_discovery(path: Path = DISCOVERY_PATH) -> dict:
    if not path.exists():
        return {"connections": [], "aliases": []}
    try:
        with path.open("rb") as stream:
            data = tomllib.load(stream)
    except (OSError, tomllib.TOMLDecodeError) as exc:
        raise IntegrityError(f"cannot parse {path.name}: {exc}") from exc

    missing = DISCOVERY_TOP_LEVEL_KEYS - set(data)
    unknown = set(data) - DISCOVERY_TOP_LEVEL_KEYS
    if missing or unknown:
        raise IntegrityError(
            f"{path.name} top-level schema missing={sorted(missing)} "
            f"unknown={sorted(unknown)}"
        )
    if type(data["schema_version"]) is not int or data["schema_version"] != 1:
        raise IntegrityError(f"{path.name} schema_version must be integer 1")
    if data["non_authoritative"] is not True:
        raise IntegrityError(f"{path.name} non_authoritative must be true")

    for container, required in (("connections", CONNECTION_KEYS), ("aliases", ALIAS_KEYS)):
        records = data[container]
        if not isinstance(records, list):
            raise IntegrityError(f"{path.name} {container} must be an array of tables")
        for position, record in enumerate(records, start=1):
            if not isinstance(record, dict):
                raise IntegrityError(
                    f"{path.name} {container}[{position}] must be a table"
                )
            record_missing = required - set(record)
            record_unknown = set(record) - required
            if record_missing or record_unknown:
                raise IntegrityError(
                    f"{path.name} {container}[{position}] keys "
                    f"missing={sorted(record_missing)} unknown={sorted(record_unknown)}"
                )
            for key, value in record.items():
                if key in DISCOVERY_INTEGER_KEYS:
                    if not isinstance(value, int) or isinstance(value, bool):
                        raise IntegrityError(
                            f"{path.name} {container}[{position}].{key} must be an integer"
                        )
                elif key == "confidence":
                    if (
                        not isinstance(value, (int, float))
                        or isinstance(value, bool)
                    ):
                        raise IntegrityError(
                            f"{path.name} {container}[{position}].confidence must be numeric"
                        )
                elif not isinstance(value, str):
                    raise IntegrityError(
                        f"{path.name} {container}[{position}].{key} must be a string"
                    )
    return data


def mask_fences(text: str) -> list[str]:
    masked: list[str] = []
    active: str | None = None
    for line in text.splitlines():
        match = FENCE_RE.match(line)
        if match:
            marker = match.group(1)[0]
            if active is None:
                active = marker
            elif marker == active:
                active = None
            masked.append("")
        elif active is None:
            masked.append(line)
        else:
            masked.append("")
    return masked


def mask_fences_and_math(text: str) -> list[str]:
    """Mask fenced code and TeX-style math while preserving line numbers.

    Inline code is deliberately retained because repository paths written in
    backticks are authored-artifact references.  Conventional Markdown links
    are retained for the same reason.
    """
    masked: list[str] = []
    code_fence: str | None = None
    math_fence: str | None = None
    for line in text.splitlines():
        code_match = FENCE_RE.match(line)
        if code_match and math_fence is None:
            marker = code_match.group(1)[0]
            if code_fence is None:
                code_fence = marker
            elif marker == code_fence:
                code_fence = None
            masked.append("")
            continue
        if code_fence is not None:
            masked.append("")
            continue

        math_match = MATH_FENCE_RE.match(line)
        if math_match:
            marker = math_match.group(1)
            if math_fence is None and marker in {"$$", r"\["}:
                math_fence = marker
            elif math_fence == "$$" and marker == "$$":
                math_fence = None
            elif math_fence == r"\[" and marker == r"\]":
                math_fence = None
            masked.append("")
            continue
        if math_fence is not None:
            masked.append("")
            continue

        masked.append(INLINE_MATH_RE.sub("", line))
    return masked


def github_slug(heading: str) -> str:
    value = re.sub(r"<[^>]*>", "", html.unescape(heading)).strip().lower()
    value = re.sub(r"[^\w\- ]", "", value, flags=re.UNICODE)
    return re.sub(r"[\s-]+", "-", value).strip("-")


def extract_sections(text: str) -> list[Section]:
    lines = text.splitlines()
    masked = mask_fences(text)
    headings: list[tuple[int, int, str, str]] = []
    slug_counts: defaultdict[str, int] = defaultdict(int)
    for index, line in enumerate(masked, start=1):
        match = HEADING_RE.match(line)
        if not match:
            continue
        heading = match.group(2).strip()
        base = github_slug(heading)
        count = slug_counts[base]
        slug_counts[base] += 1
        slug = base if count == 0 else f"{base}-{count}"
        headings.append((index, len(match.group(1)), heading, slug))

    starts = [(1, 0, "", "")] if not headings or headings[0][0] > 1 else []
    starts.extend(headings)
    sections: list[Section] = []
    for ordinal, (start, level, heading, slug) in enumerate(starts):
        end = (starts[ordinal + 1][0] - 1) if ordinal + 1 < len(starts) else len(lines)
        body = "\n".join(lines[start - 1 : end])
        sections.append(Section(ordinal, level, heading, slug, start, end, body))
    return sections


def _section_subtree_end(sections: Sequence[Section], position: int) -> int:
    """Return the last line in a heading's complete nested subsection tree."""
    section = sections[position]
    for later in sections[position + 1 :]:
        if later.level <= section.level:
            return later.start_line - 1
    return sections[-1].end_line


def _unique_heading_subtree(
    sections: Sequence[Section], heading: str
) -> tuple[Section, int] | None:
    """Resolve one literal heading conservatively, rejecting duplicates."""
    matches = [
        (position, section)
        for position, section in enumerate(sections)
        if section.heading == heading
    ]
    if len(matches) != 1:
        return None
    position, section = matches[0]
    return section, _section_subtree_end(sections, position)


def _evidence_interval_error(
    text: str,
    heading: str,
    start: object,
    end: object,
) -> str | None:
    """Validate that an interval lies inside one declared heading subtree."""
    if (
        not isinstance(start, int)
        or isinstance(start, bool)
        or not isinstance(end, int)
        or isinstance(end, bool)
    ):
        return f"line interval must contain integers, got {start!r}-{end!r}"
    sections = extract_sections(text)
    matching = [section for section in sections if section.heading == heading]
    if not matching:
        return f"evidence heading is absent: {heading!r}"
    if len(matching) != 1:
        return f"evidence heading is ambiguous ({len(matching)} occurrences): {heading!r}"
    resolved = _unique_heading_subtree(sections, heading)
    assert resolved is not None
    section, subtree_end = resolved
    if not (section.start_line <= start <= end <= subtree_end):
        return (
            f"line interval {start}-{end} is outside heading subtree "
            f"{heading!r} ({section.start_line}-{subtree_end})"
        )
    return None


def _section_at_line(sections: Sequence[Section], line: int) -> Section:
    return next(section for section in sections if section.start_line <= line <= section.end_line)


def extract_explicit_statuses(text: str, sections: Sequence[Section]) -> list[dict]:
    records: list[dict] = []
    for line_number, line in enumerate(mask_fences(text), start=1):
        match = STATUS_RE.match(line)
        if not match:
            continue
        section = _section_at_line(sections, line_number)
        records.append(
            {
                "heading": section.heading,
                "start_line": line_number,
                "end_line": line_number,
                "label": match.group("label").strip(),
                "value": match.group("value").strip().strip("*").strip(),
                "evidence": line.strip(),
            }
        )
    return records


def extract_candidate_claims(path: str, sections: Sequence[Section]) -> list[dict]:
    if path.endswith("_audit.md"):
        return []
    records: list[dict] = []
    for section in sections:
        match = CLAIM_HEADING_RE.match(section.heading)
        if not match:
            continue
        raw_kind = match.group("kind").lower()
        kind = "target" if raw_kind in {"conjecture", "open theorem"} else ("lemma" if raw_kind == "lemma" else "theorem")
        evidence = " ".join(line.strip() for line in section.body.splitlines()[1:] if line.strip())[:500]
        records.append(
            {
                "heading": section.heading,
                "start_line": section.start_line,
                "end_line": section.end_line,
                "kind": kind,
                "evidence": evidence,
            }
        )
    return records


def _clean_link_target(raw: str) -> str:
    target = raw.strip()
    if target.startswith("<") and target.endswith(">"):
        target = target[1:-1]
    else:
        target = re.sub(r"\s+(['\"]).*\1$", "", target)
    return urllib.parse.unquote(target)


def _inside_repo(root: Path, path: Path) -> bool:
    try:
        path.resolve().relative_to(root.resolve())
        return True
    except ValueError:
        return False


def extract_links(
    root: Path,
    source_relative: str,
    text: str,
    tracked: set[str],
    anchors: dict[str, set[str]],
) -> list[MarkdownLink]:
    links: list[MarkdownLink] = []
    masked_text = "\n".join(mask_fences(text))
    for match in LINK_RE.finditer(masked_text):
        line_number = masked_text.count("\n", 0, match.start()) + 1
        label, raw = re.sub(r"\s+", " ", match.group(1)).strip(), match.group(2)
        target = _clean_link_target(raw)
        parsed = urllib.parse.urlsplit(target)
        if parsed.scheme.lower() in EXTERNAL_SCHEMES or target.startswith("//"):
            links.append(MarkdownLink(line_number, label, target, None, "", "external", True, ""))
            continue
        if parsed.scheme:
            links.append(MarkdownLink(line_number, label, target, None, parsed.fragment, "external", True, ""))
            continue
        path_part = parsed.path
        fragment = parsed.fragment
        if not path_part:
            resolved = source_relative
        else:
            candidate = (root / Path(source_relative).parent / path_part).resolve()
            if not _inside_repo(root, candidate):
                links.append(MarkdownLink(line_number, label, target, None, fragment, "local", False, "target escapes repository"))
                continue
            resolved = candidate.relative_to(root.resolve()).as_posix()
        candidate_path = root / resolved
        if resolved in tracked:
            valid = True
            error = ""
        elif candidate_path.is_dir() and any(p.startswith(resolved.rstrip("/") + "/") for p in tracked):
            valid = True
            error = ""
        else:
            valid = False
            error = "target is missing or untracked"
        if valid and fragment and resolved.endswith(".md"):
            if github_slug(fragment) not in anchors.get(resolved, set()) and fragment not in anchors.get(resolved, set()):
                valid = False
                error = f"unknown fragment #{fragment}"
        links.append(MarkdownLink(line_number, label, target, resolved, fragment, "local", valid, error))
    return links


def _resolve_authored_artifact(
    root: Path,
    source_relative: str,
    raw_target: str,
    tracked: set[str],
) -> AuthoredArtifactReference | None:
    """Resolve a path which names one of the repository's authored areas."""
    target = urllib.parse.unquote(raw_target).split("#", 1)[0]
    path = PurePosixPath(target)
    if not path.parts:
        return None

    if path.parts[0] in AUTHORED_ROOTS:
        candidate = root / path
    else:
        candidate = root / PurePosixPath(source_relative).parent / path
    if not _inside_repo(root, candidate):
        return AuthoredArtifactReference(
            0, raw_target, None, False, "target escapes repository"
        )

    resolved = candidate.resolve().relative_to(root.resolve()).as_posix()
    resolved_parts = PurePosixPath(resolved).parts
    if not resolved_parts or resolved_parts[0] not in AUTHORED_ROOTS:
        return None
    if PurePosixPath(resolved).suffix.lower() not in AUTHORED_SUFFIXES:
        return None
    if resolved in tracked:
        return AuthoredArtifactReference(0, raw_target, resolved, True, "")
    return AuthoredArtifactReference(
        0,
        raw_target,
        resolved,
        False,
        "authored artifact is missing or untracked",
    )


def extract_authored_artifact_references(
    root: Path,
    source_relative: str,
    text: str,
    tracked: set[str],
) -> list[AuthoredArtifactReference]:
    """Extract authored-artifact paths from links, inline code, and prose.

    Conventional Markdown links may use same-directory paths.  Plain prose
    and inline-code references must name an authored root explicitly (for
    example ``barriers/example.md`` or ``../results/theorem.md``).
    Fenced code and TeX-style math are excluded from both scans.
    """
    masked_text = "\n".join(mask_fences_and_math(text))
    found: dict[tuple[int, str], AuthoredArtifactReference] = {}

    if source_relative.endswith(".md"):
        for pattern, target_group in ((LINK_RE, 2), (REFERENCE_LINK_RE, 1)):
            for match in pattern.finditer(masked_text):
                target = _clean_link_target(match.group(target_group))
                parsed = urllib.parse.urlsplit(target)
                if parsed.scheme or target.startswith("//") or not parsed.path:
                    continue
                reference = _resolve_authored_artifact(
                    root, source_relative, parsed.path, tracked
                )
                if reference is None:
                    continue
                line = masked_text.count("\n", 0, match.start()) + 1
                found[(line, reference.resolved_path or reference.raw_target)] = (
                    AuthoredArtifactReference(
                        line,
                        target,
                        reference.resolved_path,
                        reference.valid,
                        reference.error,
                    )
                )

    for match in AUTHORED_PATH_RE.finditer(masked_text):
        raw_target = match.group("path")
        reference = _resolve_authored_artifact(
            root, source_relative, raw_target, tracked
        )
        if reference is None:
            continue
        line = masked_text.count("\n", 0, match.start()) + 1
        key = (line, reference.resolved_path or reference.raw_target)
        found.setdefault(
            key,
            AuthoredArtifactReference(
                line,
                raw_target,
                reference.resolved_path,
                reference.valid,
                reference.error,
            ),
        )

    return sorted(found.values(), key=lambda item: (item.line, item.raw_target))


def _relative_path(value: str, field: str) -> None:
    path = PurePosixPath(value)
    if path.is_absolute() or ".." in path.parts:
        raise IntegrityError(f"{field} must be a repository-relative path: {value}")


def _opening_verdict(audit_text: str) -> str:
    lines = audit_text.splitlines()[:80]
    for first, raw_line in enumerate(lines):
        line = re.sub(r"[`*_#>]", "", raw_line).strip()
        is_verdict_label = bool(
            re.match(r"^(?:(?:OVERALL|FINAL)\s+)?VERDICT\b", line, re.I)
        )
        is_verdict_heading = raw_line.lstrip().startswith("#") and bool(
            re.search(r"\bverdict\b", line, re.I)
        )
        if is_verdict_label or is_verdict_heading:
            return "\n".join(lines[first : min(len(lines), first + 8)]).upper()
    return ""


def _has_explicit_green_verdict(audit_text: str) -> bool:
    """Return whether the opening verdict block explicitly declares GREEN.

    Accept either a line beginning with ``GREEN`` after a verdict heading, or
    a ``Verdict: GREEN`` form.  Merely mentioning GREEN elsewhere in the
    block is insufficient; in particular, negative forms such as ``not
    GREEN`` and ``no GREEN verdict`` are rejected.
    """
    block = _opening_verdict(audit_text)
    if not block:
        return False

    def positive_green_line(line: str) -> bool:
        if re.search(
            r"\b(?:RED|AMBER|PENDING)\b|"
            r"\b(?:NOT|NO)\s+(?:GREEN|AUDITED|PROVED|VALID|CORRECT|"
            r"VERIFIED|ESTABLISHED|CLAIMS?)\b",
            line,
        ):
            return False
        return bool(
            re.fullmatch(
                r"GREEN(?:"
                r"[.!](?:\s+(?!NOT\b|NO\b|RED\b|PENDING\b).+)?|"
                r"\s+(?:FOR|AT|AFTER|WITH|THROUGH|ON|AS|IN|UNDER|SUBJECT\s+TO)\b.+|"
                r",\s*(?:FOR|AT|AFTER|WITH|THROUGH|ON|AS|IN|UNDER|SUBJECT\s+TO)\b.+|"
                r"\s*[:;\-—–]\s*(?!NOT\b|NO\b|RED\b|PENDING\b).+|"
                r"\s*\((?!NOT\b|NO\b|RED\b|PENDING\b).+\)"
                r")?",
                line,
            )
        )

    for raw_line in block.splitlines():
        line = re.sub(r"[`*_#>]", "", raw_line).strip()
        if positive_green_line(line):
            return True
        match = re.fullmatch(
            r"(?:(?:OVERALL|FINAL)\s+)?VERDICT\s*[:\-—–]\s*(.+)", line
        )
        if match and positive_green_line(match.group(1)):
            return True
    return False


def _audited_source_scope(
    source_sections: Sequence[Section],
    audit_text: str,
    *,
    allow_adjacent_full_document: bool = False,
) -> tuple[str, int, int, str] | None:
    """Return a conservative contiguous source scope for an audit candidate.

    A uniquely named theorem/lemma/corollary is resolved to its complete
    subsection tree. Whole-document scope requires explicit full-file wording,
    except for the documented adjacent ``theorem.md``/``theorem_audit.md``
    convention. Ambiguous partial audits remain full-text discovery leads.
    """
    paragraphs = re.split(r"\n\s*\n", audit_text)
    audit_sections = extract_sections(audit_text)
    limiting_heading = any(
        re.search(r"(?i)\b(limitations?|exclusions?)\b", section.heading)
        for section in audit_sections
    )
    scoped_sections = [
        section.body
        for section in audit_sections
        if re.search(
            r"(?i)\b(audit|verdict|coverage|scope|limitations?|exclusions?)\b",
            section.heading,
        )
    ]
    scoped_paragraphs = [
        paragraph
        for paragraph in paragraphs
        if re.search(
            r"(?i)\b(audit|verdict|coverage|scope|green|checked)\b",
            paragraph,
        )
    ]
    declarations = "\n".join(scoped_sections + scoped_paragraphs)
    if limiting_heading:
        return None
    hard_partial = re.compile(
        r"(?i)(?:\bpartial(?:ly)?\b|\bexcept(?:ing)?\b|\bexclud(?:e|es|ed|ing)\b|"
        r"\boutside\s+(?:the\s+)?(?:audit\s+)?scope\b|"
        r"\bnot\s+(?:audited|checked|covered|verified)\b|"
        r"\bdoes\s+not\s+(?:audit|check|cover|verify)\b|"
        r"\bremains?\s+(?:open|unaudited|unchecked|unverified)\b)"
    )
    standalone_limitation = re.search(
        r"(?i)(?:\b(?:the\s+)?appendix\b[^.\n]{0,60}"
        r"(?:\bnot\s+checked\b|\bis\s+excluded\b|\bwas\s+not\b)|"
        r"\bonly\s+the\s+proof\s+is\s+checked\b)",
        audit_text,
    )
    # An adjacent filename is only a pairing hint; it must never override an
    # exclusion stated elsewhere in the audit.  Scan the complete audit text
    # before considering the adjacent-file whole-document convention.
    if hard_partial.search(audit_text) or standalone_limitation:
        return None

    named = re.findall(
        r"(?i)\b(theorem|lemma|corollary)\s+([0-9]+(?:\.[0-9]+)*)\b",
        declarations,
    )
    named = list(dict.fromkeys((kind.lower(), number) for kind, number in named))
    if len(named) > 1:
        return None
    explicit_named_scope = bool(
        named
        and re.search(
            r"(?i)(?:\b(?:audit|scope)\b[^.\n]{0,100}\b(?:checks?|covers?|audits?|verifies?|limited\s+to)\b|"
            r"\b(?:only|limited\s+to|covers?\s+only)\b[^.\n]{0,60}"
            r"\b(?:theorem|lemma|corollary)\b|"
            r"\b(?:theorem|lemma|corollary)\b[^.\n]{0,40}"
            r"(?:\bonly\b|\b(?:was|is)\s+checked\b))",
            declarations,
        )
    )
    if explicit_named_scope:
        kind, number = named[0]
        pattern = re.compile(
            rf"(?i)\b{re.escape(kind)}\s+{re.escape(number)}\b"
        )
        matches = [
            (position, section)
            for position, section in enumerate(source_sections)
            if pattern.search(section.heading)
        ]
        if len(matches) == 1:
            position, section = matches[0]
            return (
                section.heading,
                section.start_line,
                _section_subtree_end(source_sections, position),
                "explicit named-theorem subtree audit",
            )
        return None
    partial_scope = re.compile(
        r"(?i)(?:\bonly\b|\blimited\s+to\b|"
        r"\bthrough\s+(?:theorem|lemma|corollary|section)\b|"
        r"\bchecked\s+material\s+consists?\s+of\b|"
        r"\b(?:covers?|coverage|verdict|green)\b[^.\n]{0,100}\bsections?\b)"
    )
    if partial_scope.search(declarations):
        return None

    explicit_whole = re.search(
        r"(?i)(?:\b(?:audit|coverage|scope)\b[^.\n]{0,100}"
        r"\b(?:entire|whole|complete|full)\s+(?:source\s+)?(?:document|file)\b|"
        r"\bfull[- ]document\s+audit\b|\bwhole[- ]file\s+audit\b)",
        declarations,
    )
    if not explicit_whole:
        scoped_or_limiting_wording = re.search(
            r"(?i)(?:\b(?:audit|we|i)\b[^.\n]{0,60}"
            r"\b(?:checks?|checked|covers?|audits?|verifies?)\b|"
            r"\b(?:scope|coverage|limitations?|exclusions?)\b|"
            r"\b(?:theorem|lemma|corollary)\b[^.\n]{0,50}"
            r"\b(?:was|is)\s+checked\b|\bchecked\s+material\b|"
            r"\bonly\s+(?:the\s+)?(?:proof|theorem|lemma|corollary)\b)",
            declarations,
        )
        if scoped_or_limiting_wording or not allow_adjacent_full_document:
            return None
    heading = next((section.heading for section in source_sections if section.level == 1), source_sections[0].heading)
    note = (
        "explicit whole-document audit"
        if explicit_whole
        else "adjacent-filename whole-document convention"
    )
    return heading, 1, source_sections[-1].end_line, note


def _is_audit_source_declaration(audit_text: str, line: int, label: str = "") -> bool:
    """Recognize explicit declarations of the document an audit checks.

    Audit notes often cite exact hashes and links for their dependencies too.
    Those citations are useful provenance but must not create theorem/audit
    pairs.  Limit non-adjacent pairing evidence to a small local declaration
    window containing source/audit language; adjacent filenames remain the
    primary deterministic convention.
    """
    lines = audit_text.splitlines()
    start = max(0, line - 5)
    context = " ".join(lines[start:line])
    if re.search(r"(?i)\bdependenc(?:y|ies)\b", context):
        return False
    normalized_label = re.sub(r"[^a-z ]", " ", label.lower())
    normalized_label = " ".join(normalized_label.split())
    if normalized_label in {
        "source", "audited source", "audited theorem", "theorem under audit",
        "source under audit",
    }:
        return True
    immediate = " ".join(lines[max(0, line - 2):line])
    declaration = re.compile(
        r"(?i)\b(?:audited\s+(?:file|source|theorem|claim|revision)|"
        r"theorem\s+revision\s+audited|source\s+under\s+audit|"
        r"claim\s+checked|audit\s+target)\b"
    )
    if declaration.search(immediate):
        return True
    # Some audits put a path between "the audited source is" and a separate
    # Source-SHA line.  Require both parts; nearby prose about a promoted input
    # is not enough.
    return bool(
        declaration.search(context)
        and re.search(r"(?i)\bsource\s+sha(?:-256)?\b", immediate)
    )


def _proof_cycle(claims: dict[str, dict], relations: Sequence[dict]) -> list[str] | None:
    edges: defaultdict[str, list[str]] = defaultdict(list)
    indegree = {claim_id: 0 for claim_id in claims}
    for relation in relations:
        if relation["kind"] not in PROOF_RELATIONS:
            continue
        source, target = relation["source"], relation["target"]
        edges[target].append(source)  # dependency -> consumer
        indegree[source] += 1
    queue = deque(sorted(node for node, degree in indegree.items() if degree == 0))
    seen: list[str] = []
    while queue:
        node = queue.popleft()
        seen.append(node)
        for neighbour in sorted(edges[node]):
            indegree[neighbour] -= 1
            if indegree[neighbour] == 0:
                queue.append(neighbour)
    if len(seen) == len(claims):
        return None
    return sorted(node for node, degree in indegree.items() if degree > 0)


def validate_repository(root: Path = ROOT, manifest_path: Path = MANIFEST_PATH) -> list[str]:
    errors: list[str] = []
    manifest = load_manifest(manifest_path)
    discovery_path = root / "tools" / "research_discovery.toml"
    try:
        discovery = load_discovery(discovery_path)
    except IntegrityError as exc:
        errors.append(str(exc))
        discovery = {"connections": [], "aliases": []}
    tracked_tuple = tracked_paths(root)
    tracked = set(tracked_tuple)
    required_top = {"schema_version", "project", "required_text", "claims", "relations", "verifiers"}
    missing_top = required_top - set(manifest)
    unknown_top = set(manifest) - required_top
    if missing_top:
        errors.append(f"manifest is missing required top-level keys: {sorted(missing_top)}")
    if unknown_top:
        errors.append(f"manifest has unknown top-level keys: {sorted(unknown_top)}")
    if manifest.get("schema_version") != 1:
        errors.append("manifest schema_version must be 1")

    project = manifest.get("project", {})
    for key in ("status_authority", "navigation_root", "public_overview", "database"):
        if key not in project:
            errors.append(f"manifest project.{key} is required")
    if project.get("status_authority") != "RESEARCH_LEDGER.md":
        errors.append("manifest project.status_authority must remain RESEARCH_LEDGER.md")
    if project.get("navigation_root") != "active/INDEX.md":
        errors.append("manifest project.navigation_root must remain active/INDEX.md")
    if project.get("public_overview") != "README.md":
        errors.append("manifest project.public_overview must remain README.md")

    claims: dict[str, dict] = {}
    for claim in manifest.get("claims", []):
        unknown = set(claim) - CLAIM_KEYS
        if unknown:
            errors.append(f"claim {claim.get('id', '<missing>')} has unknown keys: {sorted(unknown)}")
        missing = {"id", "title", "kind", "status", "source", "active"} - set(claim)
        if missing:
            errors.append(f"claim {claim.get('id', '<missing>')} lacks {sorted(missing)}")
            continue
        claim_id = claim["id"]
        if claim_id in claims:
            errors.append(f"duplicate claim id: {claim_id}")
            continue
        claims[claim_id] = claim
        if claim["kind"] not in ALLOWED_KINDS:
            errors.append(f"claim {claim_id} has invalid kind {claim['kind']!r}")
        if claim["status"] not in ALLOWED_STATUSES:
            errors.append(f"claim {claim_id} has invalid status {claim['status']!r}")
        if claim["status"] == "conditional-target" and claim["active"]:
            errors.append(
                f"conditional target {claim_id} must have active=false"
            )
        for key in LIST_ATTRIBUTES:
            if key in claim and not isinstance(claim[key], list):
                errors.append(f"claim {claim_id}.{key} must be a list")
        for key in ("source", "audit"):
            if key not in claim:
                continue
            value = claim[key]
            try:
                _relative_path(value, f"claim {claim_id}.{key}")
            except IntegrityError as exc:
                errors.append(str(exc))
                continue
            if value not in tracked:
                errors.append(f"claim {claim_id}.{key} is missing or untracked: {value}")
        source = claim["source"]
        if source in tracked and claim.get("source_heading"):
            source_text = (root / source).read_text(encoding="utf-8", errors="replace")
            headings = {section.heading for section in extract_sections(source_text) if section.heading}
            if claim["source_heading"] not in headings:
                errors.append(
                    f"claim {claim_id} source_heading is absent from {source}: {claim['source_heading']!r}"
                )
        if source in tracked and claim.get("expected_source_sha256"):
            actual = sha256_file(root / source)
            if actual != claim["expected_source_sha256"]:
                errors.append(f"claim {claim_id} source hash drift: expected {claim['expected_source_sha256']}, got {actual}")
        if claim["status"] == "audited-green":
            audit = claim.get("audit")
            expected = claim.get("expected_source_sha256")
            if not audit or not expected:
                errors.append(f"audited-green claim {claim_id} requires audit and expected_source_sha256")
            elif audit in tracked:
                audit_text = (root / audit).read_text(encoding="utf-8")
                if expected not in audit_text:
                    errors.append(f"audit {audit} does not contain exact source hash for {claim_id}")
                if not _has_explicit_green_verdict(audit_text):
                    errors.append(f"audit {audit} lacks an unambiguous opening GREEN verdict")

    relations = manifest.get("relations", [])
    for relation in relations:
        unknown = set(relation) - RELATION_KEYS
        if unknown:
            errors.append(f"relation has unknown keys: {sorted(unknown)}")
        if not {"source", "target", "kind"} <= set(relation):
            errors.append(f"relation lacks required fields: {relation}")
            continue
        source, target, kind = relation["source"], relation["target"], relation["kind"]
        if source not in claims or target not in claims:
            errors.append(f"relation {source} -[{kind}]-> {target} has unknown endpoint")
            continue
        if source == target:
            errors.append(f"self relation is not allowed: {source} -[{kind}]-> itself")
        if kind not in ALLOWED_RELATIONS:
            errors.append(f"relation {source}->{target} has invalid kind {kind!r}")
        if kind == "uses" and claims[target]["status"] != "audited-green":
            errors.append(
                f"proved dependency {target} must be audited-green, not {claims[target]['status']}"
            )
        present_evidence = RELATION_EVIDENCE_KEYS & set(relation)
        if present_evidence and present_evidence != RELATION_EVIDENCE_KEYS:
            errors.append(f"relation {source}->{target} has incomplete evidence provenance")
        elif present_evidence:
            path = relation["evidence_source"]
            if path not in tracked:
                errors.append(f"relation {source}->{target} evidence source is missing or untracked: {path}")
            else:
                text = (root / path).read_text(encoding="utf-8", errors="replace")
                start, end = relation["evidence_start_line"], relation["evidence_end_line"]
                location_error = _evidence_interval_error(
                    text, relation["evidence_heading"], start, end
                )
                if location_error:
                    errors.append(
                        f"relation {source}->{target} evidence in {path}: {location_error}"
                    )
                actual = sha256_file(root / path)
                if actual != relation["evidence_sha256"]:
                    errors.append(f"relation {source}->{target} evidence hash drift")
    cycle = _proof_cycle(claims, relations)
    if cycle:
        errors.append(f"proof-dependency cycle among: {', '.join(cycle)}")

    markdown = [path for path in tracked_tuple if path.endswith(".md")]
    anchors: dict[str, set[str]] = {}
    for path in markdown:
        text = (root / path).read_text(encoding="utf-8", errors="replace")
        anchors[path] = {section.slug for section in extract_sections(text) if section.slug}

    strict_documents = {
        project.get("public_overview", "README.md"),
        project.get("status_authority", "RESEARCH_LEDGER.md"),
        project.get("navigation_root", "active/INDEX.md"),
    }
    for claim in claims.values():
        strict_documents.add(claim["source"])
        if claim.get("audit"):
            strict_documents.add(claim["audit"])
    for path in sorted(strict_documents):
        if path not in tracked or not path.endswith(".md"):
            continue
        text = (root / path).read_text(encoding="utf-8", errors="replace")
        for link in extract_links(root, path, text, tracked, anchors):
            if not link.valid:
                errors.append(f"{path}:{link.line}: broken link {link.raw_target!r}: {link.error}")

    navigation = project.get("navigation_root", "active/INDEX.md")
    if navigation in tracked:
        navigation_text = (root / navigation).read_text(encoding="utf-8")
        navigation_links = extract_links(root, navigation, navigation_text, tracked, anchors)
        linked = {link.resolved_path for link in navigation_links if link.valid and link.resolved_path}
        for claim_id, claim in claims.items():
            if claim["active"] and claim["source"] not in linked and claim["source"] != navigation:
                errors.append(f"active claim {claim_id} is not directly reachable from {navigation}")
        for link in navigation_links:
            source = link.resolved_path
            if not source or not source.startswith("results/") or source.endswith("_audit.md"):
                continue
            theorem = root / source
            audit_relative = theorem.with_name(theorem.stem + "_audit.md").relative_to(root).as_posix()
            if audit_relative not in tracked:
                errors.append(f"active proved input {source} lacks tracked adjacent audit {audit_relative}")
                continue
            actual_hash = sha256_file(theorem)
            audit_text = (root / audit_relative).read_text(encoding="utf-8")
            if actual_hash not in audit_text:
                errors.append(f"active proved input audit {audit_relative} lacks current source hash {actual_hash}")
            if not _has_explicit_green_verdict(audit_text):
                errors.append(f"active proved input audit {audit_relative} lacks an unambiguous opening GREEN verdict")

        active_targets = [
            claim_id
            for claim_id, claim in claims.items()
            if claim["active"] and claim["status"] == "active-target"
        ]
        if len(active_targets) != 1:
            errors.append(
                f"manifest must contain exactly one active target, found {len(active_targets)}"
            )
        input_markers = [
            line_number
            for line_number, line in enumerate(navigation_text.splitlines(), start=1)
            if line.strip().startswith("Selected audited inputs")
        ]
        barrier_markers = [
            line_number
            for line_number, line in enumerate(navigation_text.splitlines(), start=1)
            if line.strip() == "Immediate barriers:"
        ]
        if len(active_targets) == 1 and (len(input_markers) != 1 or len(barrier_markers) != 1):
            errors.append(
                "active/INDEX.md must contain exactly one Selected audited inputs section and one Immediate barriers section"
            )
        if (
            len(input_markers) == 1
            and len(barrier_markers) == 1
            and input_markers[0] >= barrier_markers[0]
        ):
            errors.append(
                "active/INDEX.md Selected audited inputs must precede Immediate barriers"
            )
        if (
            len(active_targets) == 1
            and len(input_markers) == 1
            and len(barrier_markers) == 1
            and input_markers[0] < barrier_markers[0]
        ):
            target_id = active_targets[0]
            input_start, barrier_start = input_markers[0], barrier_markers[0]
            following_heading = next(
                (
                    line_number
                    for line_number, line in enumerate(navigation_text.splitlines(), start=1)
                    if line_number > barrier_start and line.startswith("## ")
                ),
                len(navigation_text.splitlines()) + 1,
            )
            input_sources = {
                link.resolved_path
                for link in navigation_links
                if link.valid
                and link.resolved_path
                and input_start < link.line < barrier_start
                and link.resolved_path.startswith("results/")
                and not link.resolved_path.endswith("_audit.md")
            }
            barrier_sources = {
                link.resolved_path
                for link in navigation_links
                if link.valid
                and link.resolved_path
                and barrier_start < link.line < following_heading
                and link.resolved_path.startswith("barriers/")
                and not link.resolved_path.endswith("_audit.md")
            }
            relation_keys = {
                (relation["source"], relation["target"], relation["kind"])
                for relation in relations
                if {"source", "target", "kind"} <= set(relation)
            }
            claims_by_source: defaultdict[str, list[tuple[str, dict]]] = defaultdict(list)
            for claim_id, claim in claims.items():
                claims_by_source[claim["source"]].append((claim_id, claim))

            for source in sorted(input_sources):
                candidates = [
                    (claim_id, claim)
                    for claim_id, claim in claims_by_source.get(source, [])
                    if claim["active"] and claim["status"] == "audited-green"
                ]
                if not candidates:
                    errors.append(
                        f"primary proved input {source} lacks an active audited-green manifest claim"
                    )
                    continue
                if not any(
                    (target_id, claim_id, "uses") in relation_keys
                    for claim_id, _claim in candidates
                ):
                    errors.append(
                        f"primary proved input {source} lacks a direct uses relation from {target_id}"
                    )

            barrier_relation_kinds = {
                "guarded_by", "refuted_by", "sharpness_witness", "related_to"
            }
            for source in sorted(barrier_sources):
                candidates = [
                    (claim_id, claim)
                    for claim_id, claim in claims_by_source.get(source, [])
                    if claim["active"] and claim["kind"] == "barrier"
                ]
                if not candidates:
                    errors.append(
                        f"primary barrier {source} lacks an active manifest barrier claim"
                    )
                    continue
                if not any(
                    (target_id, claim_id, kind) in relation_keys
                    for claim_id, _claim in candidates
                    for kind in barrier_relation_kinds
                ):
                    errors.append(
                        f"primary barrier {source} lacks a direct guard relation from {target_id}"
                    )

            for relation in relations:
                if relation.get("source") != target_id:
                    continue
                target_claim = claims.get(relation.get("target"))
                if not target_claim:
                    continue
                if relation.get("kind") == "uses" and target_claim["source"] not in input_sources:
                    errors.append(
                        f"direct uses relation {target_id}->{relation['target']} is absent from the primary proved-input list"
                    )
                if (
                    relation.get("kind") in barrier_relation_kinds
                    and target_claim["kind"] == "barrier"
                    and target_claim["source"] not in barrier_sources
                ):
                    errors.append(
                        f"direct guard relation {target_id}->{relation['target']} is absent from the primary barrier list"
                    )

    for required in manifest.get("required_text", []):
        if set(required) != {"file", "pattern"}:
            errors.append(f"required_text entry must contain only file and pattern: {required}")
            continue
        path = required["file"]
        if path not in tracked:
            errors.append(f"required_text file is missing or untracked: {path}")
            continue
        text = (root / path).read_text(encoding="utf-8")
        if not re.search(required["pattern"], text, re.I | re.S):
            errors.append(f"{path} does not retain required status phrase /{required['pattern']}/")

    for path in tracked_tuple:
        if (root / path).suffix.lower() not in TEXT_SUFFIXES:
            continue
        text = (root / path).read_text(encoding="utf-8", errors="replace")
        if path.endswith(".md"):
            for reference in extract_authored_artifact_references(
                root, path, text, tracked
            ):
                if not reference.valid:
                    errors.append(
                        f"{path}:{reference.line}: authored artifact reference "
                        f"{reference.raw_target!r}: {reference.error}"
                    )
        for pattern in DEVICE_PATH_PATTERNS:
            match = pattern.search(text)
            if match:
                line = text.count("\n", 0, match.start()) + 1
                errors.append(f"{path}:{line}: device-specific path {match.group(0)!r}")

    if discovery_path.exists():
        if "tools/research_discovery.toml" not in tracked:
            errors.append("tools/research_discovery.toml must be Git-tracked when present")
        seen_discovery_ids: set[str] = set()
        for record_type, records in (
            ("connection", discovery["connections"]),
            ("alias", discovery["aliases"]),
        ):
            for record in records:
                record_id = record["id"]
                if record_id in seen_discovery_ids:
                    errors.append(f"duplicate discovery id: {record_id}")
                seen_discovery_ids.add(record_id)
                if not 0 <= record["confidence"] <= 1:
                    errors.append(f"discovery {record_id} confidence must be in [0,1]")
                if record["review_status"] != "needs-review":
                    errors.append(f"discovery {record_id} must remain needs-review")
                sides = [("source", record["source_path"], record["source_heading"], record.get("source_start_line", record.get("start_line")), record.get("source_end_line", record.get("end_line")), record["source_sha256"])]
                if record_type == "connection":
                    sides.append(("target", record["target_path"], record["target_heading"], record["target_start_line"], record["target_end_line"], record["target_sha256"]))
                for side, path, heading, start, end, expected_hash in sides:
                    if path not in tracked:
                        errors.append(f"discovery {record_id} {side} path is missing or untracked: {path}")
                        continue
                    text = (root / path).read_text(encoding="utf-8", errors="replace")
                    location_error = _evidence_interval_error(text, heading, start, end)
                    if location_error:
                        errors.append(
                            f"discovery {record_id} {side} in {path}: {location_error}"
                        )
                    actual_hash = sha256_file(root / path)
                    if actual_hash != expected_hash:
                        errors.append(f"discovery {record_id} {side} hash drift: expected {expected_hash}, got {actual_hash}")

    for verifier in manifest.get("verifiers", []):
        unknown = set(verifier) - {"id", "path", "sha256", "timeout", "expected_stdout", "requires"}
        if unknown:
            errors.append(f"verifier {verifier.get('id', '<missing>')} has unknown keys: {sorted(unknown)}")
        missing = {"id", "path", "sha256", "timeout", "expected_stdout"} - set(verifier)
        if missing:
            errors.append(f"verifier {verifier.get('id', '<missing>')} lacks {sorted(missing)}")
            continue
        path = verifier["path"]
        try:
            _relative_path(path, f"verifier {verifier['id']}.path")
        except IntegrityError as exc:
            errors.append(str(exc))
            continue
        if path not in tracked:
            errors.append(f"verifier {verifier['id']} is missing or untracked: {path}")
        elif sha256_file(root / path) != verifier["sha256"]:
            errors.append(f"verifier {verifier['id']} hash drift")
        if not isinstance(verifier["timeout"], int) or verifier["timeout"] <= 0:
            errors.append(f"verifier {verifier['id']} timeout must be a positive integer")
        if not isinstance(verifier["expected_stdout"], list):
            errors.append(f"verifier {verifier['id']} expected_stdout must be a list")
    return errors


def _document_area(path: str) -> str:
    first = PurePosixPath(path).parts[0]
    return first if first in {"active", "results", "barriers", "archive"} else "root"


