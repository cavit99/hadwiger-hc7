def build_index(
    root: Path = ROOT,
    manifest_path: Path = MANIFEST_PATH,
    output: Path = DEFAULT_DATABASE,
) -> Path:
    errors = validate_repository(root, manifest_path)
    if errors:
        raise IntegrityError("cannot build invalid index:\n- " + "\n- ".join(errors))
    manifest = load_manifest(manifest_path)
    discovery = load_discovery(root / "tools" / "research_discovery.toml")
    tracked_tuple = tracked_paths(root)
    tracked = set(tracked_tuple)
    markdown = [path for path in tracked_tuple if path.endswith(".md")]
    anchors = {
        path: {section.slug for section in extract_sections((root / path).read_text(encoding="utf-8", errors="replace")) if section.slug}
        for path in markdown
    }
    output.parent.mkdir(parents=True, exist_ok=True)
    descriptor, temporary_name = tempfile.mkstemp(
        prefix=f".{output.name}.", suffix=".tmp", dir=output.parent
    )
    os.close(descriptor)
    temporary = Path(temporary_name)
    connection: sqlite3.Connection | None = None
    completed = False
    try:
        connection = sqlite3.connect(temporary)
        connection.executescript(SCHEMA)
        connection.executemany(
            "INSERT INTO meta(key,value) VALUES(?,?)",
            [
                ("schema_version", "2"),
                ("git_head", git_head(root)),
                ("corpus_signature", corpus_signature(root, manifest_path, tracked_tuple)),
                ("status_authority", manifest["project"]["status_authority"]),
            ],
        )
        document_ids: dict[str, int] = {}
        document_text: dict[str, str] = {}
        document_sha: dict[str, str] = {}
        document_sections: dict[str, list[Section]] = {}
        document_links: dict[str, list[MarkdownLink]] = {}
        for path in markdown:
            raw = (root / path).read_bytes()
            text = raw.decode("utf-8", errors="replace")
            sections = extract_sections(text)
            digest = hashlib.sha256(raw).hexdigest()
            title = next((section.heading for section in sections if section.level == 1), "")
            cursor = connection.execute(
                "INSERT INTO documents(path,area,title,sha256,line_count,byte_count) VALUES(?,?,?,?,?,?)",
                (path, _document_area(path), title, digest, len(text.splitlines()), len(raw)),
            )
            document_id = cursor.lastrowid
            document_ids[path] = document_id
            document_text[path] = text
            document_sha[path] = digest
            document_sections[path] = sections
            for section in sections:
                section_cursor = connection.execute(
                    "INSERT INTO sections(document_id,ordinal,level,heading,slug,start_line,end_line,body,sha256) VALUES(?,?,?,?,?,?,?,?,?)",
                    (document_id, section.ordinal, section.level, section.heading, section.slug, section.start_line, section.end_line, section.body, hashlib.sha256(section.body.encode()).hexdigest()),
                )
                section_id = section_cursor.lastrowid
                connection.execute(
                    "INSERT INTO sections_fts(section_id,path,heading,body) VALUES(?,?,?,?)",
                    (section_id, path, section.heading, section.body),
                )
            links = extract_links(root, path, text, tracked, anchors)
            document_links[path] = links
            for link in links:
                connection.execute(
                    "INSERT INTO links(document_id,line,label,raw_target,kind,resolved_path,fragment,valid,error) VALUES(?,?,?,?,?,?,?,?,?)",
                    (document_id, link.line, link.label, link.raw_target, link.kind, link.resolved_path, link.fragment, int(link.valid), link.error),
                )
                section = _section_at_line(sections, link.line)
                if link.valid and link.resolved_path and link.resolved_path.endswith(".md") and DEPENDENCY_HEADING_RE.search(section.heading):
                    relation_id = hashlib.sha256(f"{path}\0{link.line}\0{link.resolved_path}".encode()).hexdigest()[:24]
                    connection.execute(
                        "INSERT OR IGNORE INTO candidate_relations VALUES(?,?,?,?,?,?,?,?,?,?,?,?)",
                        (f"dependency-link:{relation_id}", document_id, section.heading, link.line, link.line, digest, link.resolved_path, "uses", "dependency-section-link", 0.70, "needs-review", f"{link.label} -> {link.raw_target}"),
                    )
            for record in extract_explicit_statuses(text, sections):
                status_id = hashlib.sha256(f"{path}\0{record['start_line']}\0{record['label']}".encode()).hexdigest()[:24]
                connection.execute(
                    "INSERT INTO explicit_statuses VALUES(?,?,?,?,?,?,?,?,?,?,?,?)",
                    (f"status:{status_id}", document_id, record["heading"], record["start_line"], record["end_line"], digest, record["label"], record["value"], "explicit-line-marker", 1.0, "needs-review", record["evidence"]),
                )
            for record in extract_candidate_claims(path, sections):
                claim_id = hashlib.sha256(f"{path}\0{record['start_line']}\0{record['heading']}".encode()).hexdigest()[:24]
                connection.execute(
                    "INSERT INTO candidate_claims VALUES(?,?,?,?,?,?,?,?,?,?,?)",
                    (f"heading:{claim_id}", document_id, record["heading"], record["start_line"], record["end_line"], digest, record["kind"], "typed-heading", 0.75, "needs-review", record["evidence"]),
                )

        sha_paths: defaultdict[str, list[str]] = defaultdict(list)
        for path, digest in document_sha.items():
            sha_paths[digest].append(path)
        for audit_path in (path for path in markdown if path.endswith("_audit.md")):
            methods: defaultdict[str, set[str]] = defaultdict(set)
            audit_text = document_text[audit_path]
            adjacent = audit_path.removesuffix("_audit.md") + ".md"
            if adjacent in document_ids:
                methods[adjacent].add("adjacent-filename")
            for link in document_links[audit_path]:
                if (
                    link.valid
                    and link.resolved_path in document_ids
                    and not link.resolved_path.endswith("_audit.md")
                    and _is_audit_source_declaration(audit_text, link.line, link.label or "")
                ):
                    methods[link.resolved_path].add("explicit-markdown-link")
            for match in re.finditer(r"(?i)\b[0-9a-f]{64}\b", document_text[audit_path]):
                digest = match.group(0).lower()
                line = audit_text.count("\n", 0, match.start()) + 1
                if not _is_audit_source_declaration(audit_text, line):
                    continue
                for source_path in sha_paths.get(digest.lower(), []):
                    if not source_path.endswith("_audit.md"):
                        methods[source_path].add("exact-source-hash")
            audit_sections = document_sections[audit_path]
            audit_heading = next((s.heading for s in audit_sections if s.level == 1), audit_sections[0].heading)
            green = _has_explicit_green_verdict(audit_text)
            for source_path, pair_methods in methods.items():
                source_sections = document_sections[source_path]
                scope = _audited_source_scope(
                    source_sections,
                    audit_text,
                    allow_adjacent_full_document="adjacent-filename" in pair_methods,
                )
                if scope is None:
                    continue
                source_heading, source_start, source_end, scope_note = scope
                bound = document_sha[source_path] in audit_text
                confidence = 1.0 if green and bound and len(pair_methods) >= 2 else (0.98 if green and bound else 0.65)
                pair_id = hashlib.sha256(f"{source_path}\0{audit_path}".encode()).hexdigest()[:24]
                connection.execute(
                    "INSERT INTO audit_pairs VALUES(?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)",
                    (f"audit:{pair_id}", document_ids[source_path], document_ids[audit_path], source_heading, source_start, source_end, document_sha[source_path], audit_heading, 1, len(audit_text.splitlines()), document_sha[audit_path], "+".join(sorted(pair_methods)), confidence, "needs-review", f"Audit candidate detected by {', '.join(sorted(pair_methods))}", int(green), int(bound), scope_note),
                )
        for claim in manifest["claims"]:
            connection.execute(
                "INSERT INTO claims VALUES(?,?,?,?,?,?,?,?,?,?,?,?,?,?)",
                (
                    claim["id"], claim["title"], claim["kind"], claim["status"], claim["source"],
                    claim.get("source_heading"), int(claim["active"]), claim.get("scope"),
                    claim.get("label_fidelity"), claim.get("separator_order"),
                    claim.get("colour_compatibility"), claim.get("strict_parameter"),
                    claim.get("audit"), claim.get("expected_source_sha256"),
                ),
            )
            for category in sorted(LIST_ATTRIBUTES):
                for value in claim.get(category, []):
                    connection.execute("INSERT INTO claim_attributes VALUES(?,?,?)", (claim["id"], category, value))
        for relation in manifest["relations"]:
            connection.execute(
                "INSERT INTO relations VALUES(?,?,?,?,?,?,?,?,?)",
                (relation["source"], relation["target"], relation["kind"], relation.get("note"), relation.get("evidence_source"), relation.get("evidence_heading"), relation.get("evidence_start_line"), relation.get("evidence_end_line"), relation.get("evidence_sha256")),
            )
        for verifier in manifest["verifiers"]:
            connection.execute(
                "INSERT INTO verifiers VALUES(?,?,?,?)",
                (verifier["id"], verifier["path"], verifier["sha256"], verifier["timeout"]),
            )
        for alias in discovery["aliases"]:
            connection.execute(
                "INSERT INTO terminology_aliases VALUES(?,?,?,?,?,?,?,?,?,?,?,?,?)",
                (
                    alias["id"], alias["term"], alias["meaning"], alias["source_path"],
                    alias["source_heading"], alias["start_line"], alias["end_line"],
                    alias["source_sha256"], alias["extraction_method"], alias["confidence"],
                    alias["review_status"], alias["evidence"], alias["false_positive_warning"],
                ),
            )
        for candidate in discovery["connections"]:
            connection.execute(
                "INSERT INTO connection_candidates VALUES(?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)",
                (
                    candidate["id"], candidate["kind"], candidate["source_path"], candidate["source_heading"],
                    candidate["source_start_line"], candidate["source_end_line"], candidate["source_sha256"],
                    candidate["target_path"], candidate["target_heading"], candidate["target_start_line"],
                    candidate["target_end_line"], candidate["target_sha256"], candidate["extraction_method"],
                    candidate["confidence"], candidate["review_status"], candidate["evidence"],
                    candidate["matching"], candidate["unmatched"], candidate["rationale"],
                    candidate["warning"],
                ),
            )
        connection.commit()
        completed = True
    except sqlite3.OperationalError as exc:
        if "fts5" in str(exc).lower():
            raise IntegrityError("this Python SQLite build does not provide FTS5") from exc
        raise
    finally:
        if connection is not None:
            connection.close()
        if not completed:
            temporary.unlink(missing_ok=True)
    try:
        os.replace(temporary, output)
        return output
    finally:
        temporary.unlink(missing_ok=True)


def ensure_fresh_index(
    database: Path,
    root: Path = ROOT,
    manifest_path: Path = MANIFEST_PATH,
) -> Path:
    expected = corpus_signature(root, manifest_path)
    try:
        with closing(sqlite3.connect(database)) as connection:
            metadata = dict(connection.execute("SELECT key,value FROM meta"))
        if metadata.get("schema_version") == "2" and metadata.get("corpus_signature") == expected:
            return database
    except (sqlite3.Error, OSError):
        pass
    return build_index(root, manifest_path, database)


def search_index(database: Path, query: str, area: str | None, limit: int) -> list[tuple]:
    with closing(sqlite3.connect(database)) as connection:
        sql = """
          SELECT f.path, s.heading, s.start_line, s.end_line,
                 snippet(sections_fts, 3, '[', ']', ' … ', 18)
          FROM sections_fts AS f
          JOIN sections AS s ON s.id = f.section_id
          JOIN documents AS d ON d.id = s.document_id
          WHERE sections_fts MATCH ?
        """
        params: list[object] = [query]
        if area:
            sql += " AND d.area = ?"
            params.append(area)
        sql += " ORDER BY bm25(sections_fts), f.path, s.start_line LIMIT ?"
        params.append(limit)
        return connection.execute(sql, params).fetchall()


def _claim_map(manifest: dict) -> dict[str, dict]:
    return {claim["id"]: claim for claim in manifest["claims"]}


def _relation_groups(manifest: dict) -> tuple[dict[str, list[dict]], dict[str, list[dict]]]:
    outgoing: defaultdict[str, list[dict]] = defaultdict(list)
    incoming: defaultdict[str, list[dict]] = defaultdict(list)
    for relation in manifest["relations"]:
        outgoing[relation["source"]].append(relation)
        incoming[relation["target"]].append(relation)
    return outgoing, incoming


def proof_dag(manifest: dict) -> str:
    claims = _claim_map(manifest)
    lines = ["# Generated current proof graph", "", "> Non-authoritative; generated from `tools/research_manifest.toml`.", "", "```mermaid", "graph TD"]
    for claim_id in sorted(claims):
        claim = claims[claim_id]
        style = "target" if claim["kind"] == "target" else claim["kind"]
        label = claim["title"].replace('"', "'")
        lines.append(f'  {re.sub(r"[^A-Za-z0-9_]", "_", claim_id)}["{label}"]:::{style}')
    for relation in sorted(manifest["relations"], key=lambda item: (item["kind"], item["target"], item["source"])):
        if relation["kind"] not in PROOF_RELATIONS:
            continue
        dependency = re.sub(r"[^A-Za-z0-9_]", "_", relation["target"])
        consumer = re.sub(r"[^A-Za-z0-9_]", "_", relation["source"])
        lines.append(f'  {consumer} -->|"{relation["kind"]}"| {dependency}')
    lines.extend(["  classDef theorem fill:#d8f3dc,stroke:#2d6a4f", "  classDef lemma fill:#d8f3dc,stroke:#2d6a4f", "  classDef target fill:#fff3bf,stroke:#d9480f", "  classDef barrier fill:#ffe3e3,stroke:#c92a2a", "  classDef laboratory fill:#e7f5ff,stroke:#1971c2", "```", ""])
    return "\n".join(lines)


def trust_boundaries(manifest: dict) -> str:
    lines = ["# Generated trust boundaries", "", "> Non-authoritative; generated from `tools/research_manifest.toml`.", "", "| Claim | Status | Conclusions / allowed exits | Does not prove |", "|---|---|---|---|"]
    for claim in sorted((item for item in manifest["claims"] if item["active"]), key=lambda item: item["id"]):
        outcomes = claim.get("conclusions", []) or claim.get("allowed_exits", [])
        conclusions = "; ".join(outcomes) or "—"
        gaps = "; ".join(claim.get("does_not_prove", [])) or "—"
        lines.append(f"| `{claim['id']}` | {claim['status']} | {conclusions} | {gaps} |")
    lines.append("")
    return "\n".join(lines)


def barrier_matrix(manifest: dict) -> str:
    lines = ["# Generated barrier matrix", "", "> Non-authoritative; generated from `tools/research_manifest.toml`.", "", "| Barrier | Refutes | Satisfies | Lacks |", "|---|---|---|---|"]
    for claim in sorted((item for item in manifest["claims"] if item["kind"] == "barrier"), key=lambda item: item["id"]):
        lines.append(
            f"| `{claim['id']}` | {'; '.join(claim.get('refutes', [])) or '—'} | {'; '.join(claim.get('satisfies', [])) or '—'} | {'; '.join(claim.get('lacks', [])) or '—'} |"
        )
    lines.append("")
    return "\n".join(lines)


def _cell(value: object) -> str:
    return str(value if value not in (None, "") else "—").replace("|", "\\|").replace("\n", " ")


def orphaned_audited_results(database: Path) -> str:
    with closing(sqlite3.connect(database)) as connection:
        rows = connection.execute(
            """
            SELECT ds.path, ap.source_heading, ap.source_start_line, ap.source_end_line,
                   ap.source_sha256, GROUP_CONCAT(DISTINCT da.path), MAX(ap.confidence)
            FROM audit_pairs ap
            JOIN documents ds ON ds.id=ap.source_document_id
            JOIN documents da ON da.id=ap.audit_document_id
            WHERE ds.area='results' AND ap.green_verdict=1 AND ap.source_hash_bound=1
              AND NOT EXISTS (SELECT 1 FROM claims c WHERE c.source_path=ds.path)
            GROUP BY ds.path, ap.source_heading, ap.source_start_line, ap.source_end_line, ap.source_sha256
            ORDER BY ds.path
            """
        ).fetchall()
    lines = ["# Orphaned hash-current audited-result candidates", "", "> Non-authoritative inventory. Every row remains `needs-review`; audit scope must be checked before curation.", "", "| Source | Heading/lines | Audit candidate | SHA-256 | Confidence |", "|---|---|---|---|---:|"]
    for path, heading, start, end, digest, audits, confidence in rows:
        lines.append(f"| `{path}` | {_cell(heading)} ({start}–{end}) | `{audits}` | `{digest}` | {confidence:.2f} |")
    lines.extend(["", f"Total: **{len(rows)}** candidates.", ""])
    return "\n".join(lines)


